#include <stm32f10x.h>                  /* STM32F10x definitions              */
#include <stdint.h>
#include "KBD.h"

#define NUM_KEYS  2                     /* Number of available keys           */

/* Keys for MCBSTM32 */
#define TAMP    1
#define WKUP    2


/*-----------------------------------------------------------------------------
 *       KBD_Init:  Initialize keyboard/buttons
 *
 * Parameters: (none)
 * Return:     (none)
 *----------------------------------------------------------------------------*/
void KBD_Init (void) {
  /* Initialization is done in STM32_Init.c */
}


/*-----------------------------------------------------------------------------
 *       KBD_UnInit:  Uninitialize keyboard/buttons
 *
 * Parameters: (none)
 * Return:     (none)
 *----------------------------------------------------------------------------*/
void KBD_UnInit (void) {
}


/*-----------------------------------------------------------------------------
 *       KBD_GetKeys:  Get keyboard state
 *
 * Parameters: (none)
 * Return:      Keys bitmask
 *----------------------------------------------------------------------------*/
uint32_t KBD_GetKeys (void) {
  /* Read board keyboard inputs */
  uint32_t val = 0;

  if ((GPIOC->IDR & 0x2000) != 0) {
    /* TAMP button - PC13 */
    val |= TAMP;
  }
  if ((GPIOA->IDR & 0x0001) == 0) {
    /* WKUP button - PA0 */
    val |= WKUP;
  }
  return (val);
}


/*-----------------------------------------------------------------------------
 *       KBD_Num:  Get number of available keys
 *
 * Parameters: (none)
 * Return:      number of keys
 *----------------------------------------------------------------------------*/
uint32_t KBD_Num (void) {
  return (NUM_KEYS);
}

