#include <stdio.h>
#include <MINI51.h>


void delay_loop(void)
{
	uint32_t j;
	for(j=0;j<60000;j++);
	for(j=0;j<60000;j++);
	for(j=0;j<60000;j++);
	for(j=0;j<60000;j++);
}


/*---------------------------------------------------------------------------------------------------------*/
/* MAIN	function																						  */
/*---------------------------------------------------------------------------------------------------------*/

int	main (void)
{

  SystemCoreClockUpdate();

	/* set GPE output */
  _GPIO_SET_PIN_MODE(P3, 6, GPIO_PMD_OUTPUT);

	while(1)
	{
    P36 = 0;
		delay_loop();
    P36 = 1;
		delay_loop();
	}
}

